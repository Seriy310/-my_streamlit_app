import streamlit as st
import pandas as pd
import os
from datetime import datetime

st.set_page_config(page_title="–Ü—Å—Ç–æ—Ä—ñ—è –≤–∏—Ç—Ä–∞—Ç", layout="centered")
st.title("üìú –Ü—Å—Ç–æ—Ä—ñ—è –≤–∏—Ç—Ä–∞—Ç")

file_name = "expenses_history.csv"

if not os.path.exists(file_name):
    st.write("‚õî –ù–µ–º–∞—î –¥–∞–Ω–∏—Ö –¥–ª—è –≤—ñ–¥–æ–±—Ä–∞–∂–µ–Ω–Ω—è.")
    df = pd.DataFrame(columns=["–ù–∞–∑–≤–∞", "–°—É–º–∞", "–î–∞—Ç–∞"])
else:
    df = pd.read_csv(file_name)

df["–î–∞—Ç–∞"] = pd.to_datetime(df["–î–∞—Ç–∞"], errors='coerce').dt.date
df["–°—É–º–∞"] = pd.to_numeric(df["–°—É–º–∞"], errors='coerce').fillna(0.0)

# üîπ –§—ñ–ª—å—Ç—Ä–∞—Ü—ñ—è –∑–∞ –¥–∞—Ç–æ—é
st.subheader("üìÖ –§—ñ–ª—å—Ç—Ä –∑–∞ –¥–∞—Ç–æ—é")
col1, col2 = st.columns(2)
min_date = col1.date_input("–ü–æ—á–∞—Ç–∫–æ–≤–∞ –¥–∞—Ç–∞", df["–î–∞—Ç–∞"].min() if not df.empty else datetime.today())
max_date = col2.date_input("–ö—ñ–Ω—Ü–µ–≤–∞ –¥–∞—Ç–∞", df["–î–∞—Ç–∞"].max() if not df.empty else datetime.today())

# üîπ –§—ñ–ª—å—Ç—Ä–∞—Ü—ñ—è –∑–∞ —Å—É–º–æ—é
st.subheader("üí∞ –§—ñ–ª—å—Ç—Ä –∑–∞ —Å—É–º–æ—é")
col3, col4 = st.columns(2)
min_amount = col3.number_input("–ú—ñ–Ω—ñ–º–∞–ª—å–Ω–∞ —Å—É–º–∞ (z≈Ç)", min_value=0.0, value=df["–°—É–º–∞"].min() if not df.empty else 0.0)
max_amount = col4.number_input("–ú–∞–∫—Å–∏–º–∞–ª—å–Ω–∞ —Å—É–º–∞ (z≈Ç)", min_value=0.0, value=df["–°—É–º–∞"].max() if not df.empty else 10000.0)

# üîπ –ó–∞—Å—Ç–æ—Å—É–≤–∞–Ω–Ω—è —Ñ—ñ–ª—å—Ç—Ä—ñ–≤
df_filtered = df[
    (df["–î–∞—Ç–∞"] >= min_date) & (df["–î–∞—Ç–∞"] <= max_date) &
    (df["–°—É–º–∞"] >= min_amount) & (df["–°—É–º–∞"] <= max_amount)
]

st.write(f"üîç –ó–Ω–∞–π–¥–µ–Ω–æ {len(df_filtered)} –∑–∞–ø–∏—Å—ñ–≤")
st.dataframe(df_filtered)

